# relational operator (<,<=,>,>=,==,!=)  - will return boolean value

num1 = 100
num2 = 200

print(num1>num2) # 100>200 false
print(num1<num2) # 100<200 true
print(num1 == num2) # 2 equals means comparing,
#                     single = means assining right side value to left variable
print(num1<=num2) # true
print(num1 !=0) # true
