# program to convert degree celcius to farenhight
# (32°F − 32) × 5/9 = 0°C
temp_in_celcius = int(input("Enter temp. in celcius "))
temp_in_farenheit = (temp_in_celcius * 2 +30)
print(f" {temp_in_celcius} degree celcius is {temp_in_farenheit} degree farenheit")

